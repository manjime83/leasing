
package co.com.assist.leasing.model;

import java.math.BigDecimal;


public class Resolucion {

	protected String apellidosInfractores;
	protected String estadosResoluciones;
	protected String fechaComparendo;
	protected String fechaResolucion;
	protected String idSecretarias;
	protected String nombresInfractores;
	protected String numeroComparendo;
	protected String resoluciones;
	protected String secretarias;
	protected BigDecimal total;

	public String getApellidosInfractores() {
		return apellidosInfractores;
	}

	public void setApellidosInfractores(String apellidosInfractores) {
		this.apellidosInfractores = apellidosInfractores;
	}

	public String getEstadosResoluciones() {
		return estadosResoluciones;
	}

	public void setEstadosResoluciones(String estadosResoluciones) {
		this.estadosResoluciones = estadosResoluciones;
	}

	public String getFechaComparendo() {
		return fechaComparendo;
	}

	public void setFechaComparendo(String fechaComparendo) {
		this.fechaComparendo = fechaComparendo;
	}

	public String getFechaResolucion() {
		return fechaResolucion;
	}

	public void setFechaResolucion(String fechaResolucion) {
		this.fechaResolucion = fechaResolucion;
	}

	public String getIdSecretarias() {
		return idSecretarias;
	}

	public void setIdSecretarias(String idSecretarias) {
		this.idSecretarias = idSecretarias;
	}

	public String getNombresInfractores() {
		return nombresInfractores;
	}

	public void setNombresInfractores(String nombresInfractores) {
		this.nombresInfractores = nombresInfractores;
	}

	public String getNumeroComparendo() {
		return numeroComparendo;
	}

	public void setNumeroComparendo(String numeroComparendo) {
		this.numeroComparendo = numeroComparendo;
	}

	public String getResoluciones() {
		return resoluciones;
	}

	public void setResoluciones(String resoluciones) {
		this.resoluciones = resoluciones;
	}

	public String getSecretarias() {
		return secretarias;
	}

	public void setSecretarias(String secretarias) {
		this.secretarias = secretarias;
	}

	public BigDecimal getTotal() {
		return total;
	}

	public void setTotal(BigDecimal total) {
		this.total = total;
	}

}
